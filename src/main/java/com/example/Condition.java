package com.example;

public class Condition {
    public static void main(String[] args) {
        for(int i=0;i<=20;i++){
            if (i%2==0)
            {
                System.out.println("Even Number:"+i);
            }
            else{
                System.out.println("Odd Number:"+i);
            }
        }
    
    }}
